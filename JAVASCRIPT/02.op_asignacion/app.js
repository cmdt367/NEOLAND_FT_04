//Operador de asignación -> =
let x = 10
console.log(x)

//Operador de suma y asignación -> +=
//x = x + 5
x += 5
console.log(x)

//Operador de resta y asignación -> -=
x -= 2
console.log(x)

//Operador de multiplicación y asignación -> *=
x *= 2
console.log(x)

//Operador de división y asignación -> /=
x /= 3
console.log(x)

//Autosuma y autoresta -> -- ++
x++
console.log(x)
